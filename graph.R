data <- read.csv("trie_vs_hash.csv", sep="\t", header=TRUE)
par(mfrow=c(2,2))
seq1 <- subset(data, type == "seq1")
plot(seq1$items/1000, 1/seq1$t_ins, type="l", lwd=1, lty=1, xlab="Items (k)", ylab="Items per second (M) (M)", log="x", ylim=c(0, 20), main="Sequence1, insert")
lines(seq1$items/1000, 1/seq1$h_ins, lwd=1, lty=2)
lines(seq1$items/1000, 1/seq1$hs_ins, lwd=1, lty=3)
lines(seq1$items/1000, 1/seq1$sl_ins, lwd=1, lty=4)
legend(x=10, y=20, legend=c("fixedkeytrie", "STL unordered_map", "JudyHS", "JudySL"), lwd=1, lty=c(1,2,3,4), cex=1/1.2)
plot(seq1$items/1000, 1/seq1$t_look, type="l", lwd=1, lty=1, xlab="Items (k)", ylab="Items per second (M)", log="x", ylog=TRUE, ylim=c(0, 20), main="Sequence1, lookup")
lines(seq1$items/1000, 1/seq1$h_look, lwd=1, lty=2)
lines(seq1$items/1000, 1/seq1$hs_look, lwd=1, lty=3)
lines(seq1$items/1000, 1/seq1$sl_look, lwd=1, lty=4)
legend(x=10, y=20, legend=c("fixedkeytrie", "STL unordered_map", "JudyHS", "JudySL"), lwd=1, lty=c(1,2,3,4), cex=1/1.2)
plot(seq1$items/1000, 1/(seq1$t_ins+seq1$t_look), type="l", lwd=1, lty=1, xlab="Items (k)", ylab="Items per second (M)", log="x", ylog=TRUE, ylim=c(0, 20), main="Sequence1, insert+lookup")
lines(seq1$items/1000, 1/(seq1$h_ins+seq1$h_look), lwd=1, lty=2)
lines(seq1$items/1000, 1/(seq1$hs_ins+seq1$hs_look), lwd=1, lty=3)
lines(seq1$items/1000, 1/(seq1$sl_ins+seq1$sl_look), lwd=1, lty=4)
legend(x=10, y=20, legend=c("fixedkeytrie", "STL unordered_map", "JudyHS", "JudySL"), lwd=1, lty=c(1,2,3,4), cex=1/1.2)
plot(seq1$items/1000, seq1$t_mem, type="l", lwd=1, lty=1, xlab="Items (k)", ylab="Memory (MB)", log="x", ylog=TRUE, ylim=c(1, 1000), main="Sequence1, memory consumed")
lines(seq1$items/1000, seq1$h_mem, lwd=1, lty=2)
lines(seq1$items/1000, seq1$hs_mem, lwd=1, lty=3)
lines(seq1$items/1000, seq1$sl_mem, lwd=1, lty=4)
legend(x=10, y=1000, legend=c("fixedkeytrie", "STL unordered_map", "JudyHS", "JudySL"), lwd=1, lty=c(1,2,3,4), cex=1/1.2)
#dev.copy2eps(file="sequence1.eps")
dev.copy(png, "sequence1.png")
dev.off();
